import turtle
turtle.delay(8)
t = turtle.Turtle()
turtle.setup(800,500)


t.color("red")
t.begin_fill()
t.forward(150)
t.left(90)
t.forward(150)
t.left(90)
t.forward(300)
t.left(90)
t.end_fill()

t.color("blue")
t.penup()
t.forward(150)
t.pendown()
t.begin_fill()
t.forward(150)
t.left(90)
t.forward(300)
t.left(90)
t.forward(150)
t.end_fill()

t.color("black")
t.forward(300)
t.left(90)
t.forward(300)
t.left(90)
t.forward(300)
t.left(90)
t.forward(150)
turtle.exitonclick()